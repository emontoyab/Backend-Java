package library;

public class Client {
    public String name;
    public String lastname;
    public String address;
    public String phone;
    public String email;
    public String birthDate;

    public Client(
        String name, 
        String lastname, 
        String address,
        String phone,
        String email,
        String birthDate
        )
        {
            this.name = name;
            this.lastname = lastname;
            this.address = address;
            this.phone = phone;
            this.email = email;
            this.birthDate = birthDate;
        }

    
}
